/**
 * Ray-casting point-in-polygon test.
 * Returns true if the point (lat, lng) falls inside the GenSan boundary.
 */
export function isInsideGensan(lat: number, lng: number): boolean {
  const ring = COORDS;
  let inside = false;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const xi = ring[i][0],
      yi = ring[i][1];
    const xj = ring[j][0],
      yj = ring[j][1];
    if ((yi > lat) !== (yj > lat) && lng < ((xj - xi) * (lat - yi)) / (yj - yi) + xi) {
      inside = !inside;
    }
  }
  return inside;
}
